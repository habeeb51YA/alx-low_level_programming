/**
 * op_add - adds two values @a and @b
 *
 * @a: value a
 * @b: value b
 *
 * Return: sum of @a and @b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - minus two values @a and @b
 *
 * @a: value a
 * @b: value b
 *
 * Return: reminder of @a and @b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two values @a and @b
 *
 * @a: value a
 * @b: value b
 *
 * Return: multiplication of @a and @b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two values @a and @b
 *
 * @a: value a
 * @b: value b
 *
 * Return: division of @a and @b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - reminder of two values @a and @b
 *
 * @a: value a
 * @b: value b
 *
 * Return: remainder of @a and @b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
